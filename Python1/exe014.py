# "Celsius → Fahrenheit - # Multiplicamos a temperatura em ºC por 1,8 e somamos 32 ao resultado.
# Ex # Converter a temperatura de 15 ºC para ºF. # 15 x 1,8 = 27 + 32 = 59 ºF

# Fahrenheit → Celsius # Subtraímos a temperatura em ºF por 32 e dividimos o resultado por 1,8.
# Ex # Converter a temperatura de 40 ºF para ºC. # 40 – 32 = 8 : 1,8 = 4,4 ºC"

